let friends = 100;
friends = friends + 5;
console.log(`The total number of friends is ${friends}.`);
friends = friends - 3;
console.log(`After losing some friends, the total number of friends is ${friends}.`);
friends = friends * 2;
console.log(`After making new friends, the total number of friends is ${friends}.`);
friends = friends / 4;
console.log(`After sharing friends, the total number of friends is ${friends}.`);
friends = friends ** 2
console.log(`After exponenting friends , the total number of friends is ${friends}.`);
friends = friends % 3;
console.log(`After taking the remainder of friends, the total number of friends is ${friends}.`);


